 # Extensiones sobre protocolos
 ### Donde está la chicha.
 
 Yendo directo al granos, las extensiones sobre protocolos nos permiten salvaguardar una de las mejores ventajas de subclasear a la vez que ganamos todo lo que nos aportan los protocolos: la herencia.
 
 Las extensiones sobre protocolos nos permiten definir una serie de instrucciones por defecto que nos harán ahorrar mucho código. Además, el tipo que conforma siempre podrá aportar una versión más específica de esta instrucción, actuando similar a un override en una subclase (sin todas las desventajas que tiene hacer esto en una subclase).
 
 Unido esto a las limitaciones por tipo que podemos aplicar a nuestras extensiones, se nos presenta una herramienta tan potente como flexible y que la mayoría de veces presentarán la forma más adecuada de escribir nuestro código (aka the *swifty way*).
